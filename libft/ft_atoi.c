#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	r;
	int			sign;
	int			i;

	i = 0;
	r = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str && (*str >= '0' && *str <= '9'))
	{
		r = r * 10 + (*str - '0');
		str++;
		i++;
	}
	return ((int)(r * sign));
}
